/**
 * @module digest-reporter
 * @description Distribution Digest (Story 5.5) — Generates daily distribution
 * reports showing what content was routed to which mind clones.
 *
 * Complementary to the pipeline daily digest (Epic 4) which reports ingestion.
 * This digest reports distribution and routing decisions.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DEFAULT_DATA_DIR = path.resolve(__dirname, '../../hydra-data');

/**
 * @typedef {Object} DistributionRecord
 * @property {string} contentId
 * @property {string} title
 * @property {string} tier
 * @property {string[]} cloneIds - Clone IDs that received this content
 * @property {string[]} projects - Projects impacted
 * @property {number} avgRelevance
 */

/**
 * Distribution digest collector.
 * Accumulates routing decisions during a pipeline run and generates a report.
 */
export class DigestReporter {
  /**
   * @param {Object} [options]
   * @param {string} [options.dataDir] - Hydra data directory
   */
  constructor(options = {}) {
    this.dataDir = options.dataDir || DEFAULT_DATA_DIR;
    /** @type {DistributionRecord[]} */
    this.records = [];
    this.startTime = Date.now();
    this.entityStats = { total: 0, new: 0 };
  }

  /**
   * Record a routing decision.
   * @param {Object} routing - ContentRouteResult from router
   * @param {Object} content - Content metadata
   */
  record(routing, content) {
    if (!routing || !routing.targetClones) return;

    const avgRelevance = routing.targetClones.length > 0
      ? routing.targetClones.reduce((sum, c) => sum + c.relevanceScore, 0) / routing.targetClones.length
      : 0;

    this.records.push({
      contentId: routing.contentId,
      title: content.title || 'Untitled',
      tier: content.tier || 'B',
      cloneIds: routing.targetClones.map(c => c.id),
      projects: routing.targetProjects || [],
      avgRelevance: Math.round(avgRelevance * 1000) / 1000,
    });
  }

  /**
   * Set entity indexing stats.
   * @param {number} total
   * @param {number} newEntities
   */
  setEntityStats(total, newEntities) {
    this.entityStats = { total, new: newEntities };
  }

  /**
   * Generate distribution digest markdown.
   * @param {Object} [options]
   * @param {string} [options.date] - Override date
   * @returns {string}
   */
  generateDigest(options = {}) {
    const date = options.date || new Date().toISOString().split('T')[0];
    const totalItems = this.records.length;

    // Count unique clones
    const cloneSet = new Set();
    const cloneCounts = {};
    const cloneRelevances = {};
    const cloneDepartments = {};
    const projectSet = new Set();

    for (const record of this.records) {
      for (const cloneId of record.cloneIds) {
        cloneSet.add(cloneId);
        cloneCounts[cloneId] = (cloneCounts[cloneId] || 0) + 1;
        if (!cloneRelevances[cloneId]) cloneRelevances[cloneId] = [];
        cloneRelevances[cloneId].push(record.avgRelevance);
      }
      for (const project of record.projects) {
        projectSet.add(project);
      }
    }

    const totalClones = cloneSet.size;
    const avgRelevance = this.records.length > 0
      ? this.records.reduce((sum, r) => sum + r.avgRelevance, 0) / this.records.length
      : 0;

    // Top recipients
    const topRecipients = Object.entries(cloneCounts)
      .map(([id, count]) => ({
        id,
        count,
        avgRelevance: cloneRelevances[id].reduce((a, b) => a + b, 0) / cloneRelevances[id].length,
      }))
      .sort((a, b) => b.count - a.count)
      .slice(0, 10);

    // Items distributed
    const itemsList = this.records
      .map((r, i) => `${i + 1}. "${r.title}" (Tier ${r.tier}) -> ${r.cloneIds.join(', ')}`)
      .join('\n');

    const md = `# Distribution Digest -- ${date}

## Summary
- Items distributed: ${totalItems}
- Mind clones enriched: ${totalClones}
- Projects impacted: ${projectSet.size}
- Average relevance score: ${avgRelevance.toFixed(2)}
- Entities indexed: ${this.entityStats.total} (${this.entityStats.new} new)

## Top Recipients
| Clone | Items | Avg Relevance |
|-------|-------|--------------|
${topRecipients.map(r => `| ${r.id} | ${r.count} | ${r.avgRelevance.toFixed(2)} |`).join('\n') || '| (none) | 0 | 0 |'}

## Projects Impacted
${[...projectSet].map(p => `- ${p}`).join('\n') || '- (none)'}

## Items Distributed
${itemsList || '(none)'}

---
*Generated by HYDRA Distribution at ${new Date().toISOString()}*
`;

    return md;
  }

  /**
   * Save digest to file.
   * @param {Object} [options]
   * @param {string} [options.date] - Override date
   * @returns {{ path: string, saved: boolean, error?: string }}
   */
  save(options = {}) {
    const date = options.date || new Date().toISOString().split('T')[0];
    const digestDir = path.join(this.dataDir, 'digests', 'distribution');

    try {
      if (!fs.existsSync(digestDir)) {
        fs.mkdirSync(digestDir, { recursive: true });
      }

      const digestPath = path.join(digestDir, `${date}.md`);
      const content = this.generateDigest({ date });
      fs.writeFileSync(digestPath, content, 'utf-8');

      return { path: digestPath, saved: true };
    } catch (error) {
      return { path: '', saved: false, error: error.message };
    }
  }

  /**
   * Load and display a digest from file.
   * @param {Object} [options]
   * @param {string} [options.date] - Date to load (default: today)
   * @returns {string|null}
   */
  load(options = {}) {
    const date = options.date || new Date().toISOString().split('T')[0];
    const digestPath = path.join(this.dataDir, 'digests', 'distribution', `${date}.md`);

    if (!fs.existsSync(digestPath)) {
      return null;
    }

    return fs.readFileSync(digestPath, 'utf-8');
  }

  /**
   * Get summary line for pipeline output.
   * @returns {string}
   */
  getSummary() {
    const cloneSet = new Set();
    for (const r of this.records) {
      for (const c of r.cloneIds) cloneSet.add(c);
    }
    return `Distribution: ${this.records.length} items -> ${cloneSet.size} clones`;
  }
}
